export type TranslationKey = keyof typeof STRINGS.tr;

export function t(
  lang: Language,
  key: TranslationKey,
  params?: Record<string, string | number>
): string {
  const str: string =
    (STRINGS[lang] as Record<string, string>)[key as string] ??
    (STRINGS.tr as Record<string, string>)[key as string] ??
    (key as string);
  if (!params) return str;
  return Object.entries(params).reduce(
    (s, [k, v]) => s.replace(`{${k}}`, String(v)),
    str
  );
}
